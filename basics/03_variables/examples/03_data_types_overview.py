# --------------------------------------------------------
# Integers
# --------------------------------------------------------
count = 10
year = 2025

print(count)
print(year)

# Integers are commonly used in arithmetic expressions.
a = 7
b = 3

result = a - b
print(result)

# --------------------------------------------------------
# Floating-point numbers
# --------------------------------------------------------
price = 19.99
temperature = -3.5

print(price)
print(temperature)

# Division between numbers may produce a floating-point result.
x = 10
y = 4

print(x / y)
